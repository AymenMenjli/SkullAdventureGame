		function levelCheck() {
			if (heroShip.x > doorLevel.x + 500) {
				switch (dataPlayer.gameLevel) {
					case "Level0":
						dataPlayer.gameLevel = "Level1";
						break;
					case "Level1":
						dataPlayer.gameLevel = "Level2";
						break;
					case "Level2":
						dataPlayer.gameLevel = "Level3";
						break;
					case "Level3":
						dataPlayer.gameLevel = "Level4";
						break;
				}
				var superCountString = "" + heroShip.superCount;
				dataPlayer.superCount = superCountString;
				if (dataPlayer.gameLevel === "Level4") {
					dataPlayer.gameState = "newGame";
					_stage.gotoAndStop("win");
				} else {
					dataPlayer.gameState = "continue";
				}
				dataPlayer.score = '' + heroShip.score + '';
				localStorage.setItem("playerData", JSON.stringify(playerData));
				if (dataPlayer.gameLevel === "Level4") {
					dataPlayer.gameLevel = "Level0";
					localStorage.setItem("playerData", JSON.stringify(playerData));
				} else {
					_stage.gotoAndStop("endGame");
				}
				loadMap();
			} else if (removeShip && heroShip.life === 0) {
				superCountString = "" + heroShip.superCount;
				dataPlayer.superCount = superCountString;
				dataPlayer.gameState = "continue";
				localStorage.setItem("playerData", JSON.stringify(playerData));
				loadMap();
				_stage.gotoAndStop("start");
			}
		}
		
